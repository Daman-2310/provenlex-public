# ── BatchTransactionRequest ───────────────────────────────────────────────────


class BatchTransactionRequest(_BaseContract):
    """
    Payload for ``POST /api/v1/transactions/batch`` — submit multiple
    transactions in a single atomic ingress call.

    Each element in ``transactions`` is independently validated as a
    ``TransactionRequest``.  If any element fails validation the entire batch
    is rejected with the per-element error detail.

    Args:
        transactions: List of 1–50 ``TransactionRequest`` objects.
        batch_id: Client-generated unique identifier for this batch.
        tenant_id: Tenant submitting the batch; must match every transaction's
            ``tenant_id`` field to prevent cross-tenant injection via batch.

    Raises:
        ValidationError: If the list is empty, exceeds 50 items, any element
            fails its own validation, or any transaction's tenant_id mismatches
            ``batch_id``'s ``tenant_id``.
    """

    transactions: Annotated[
        list[TransactionRequest],
        Field(min_length=1, max_length=_MAX_BATCH_SIZE),
    ]
    batch_id: Annotated[
        str,
        Field(min_length=8, max_length=64, pattern=r"^batch-[a-zA-Z0-9]{1,57}$"),
    ]
    tenant_id: TenantIDStr

    @model_validator(mode="after")
    def _check_tenant_consistency(self) -> "BatchTransactionRequest":
        """Every transaction in the batch must belong to the same tenant."""
        mismatches = [
            (i, tx.tenant_id)
            for i, tx in enumerate(self.transactions)
            if tx.tenant_id != self.tenant_id
        ]
        if mismatches:
            detail = ", ".join(f"[{i}]={tid!r}" for i, tid in mismatches[:5])
            raise ValueError(
                f"Batch tenant_id={self.tenant_id!r} but transactions have "
                f"different tenant IDs: {detail}"
            )
        return self


