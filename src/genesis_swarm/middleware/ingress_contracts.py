class TransactionRequest(_BaseContract):
    """
    Payload for ``POST /api/v1/transactions`` — submit a transaction to the
    PBFT consensus pipeline.

    Args:
        tx_id: Unique transaction ID matching ``tx-<8-32 alphanum>``.
        tx_type: Discriminator determining which PBFT consensus ruleset applies.
        tenant_id: The originating tenant's identifier.
        initiator_bot: The bot type that generated this transaction.
        score: Bot's confidence score for this event in [0.0, 100.0].
        amount: Optional monetary amount; required for financial transaction types.
        currency: ISO 4217 code; required when ``amount`` is provided.
        timestamp_ns: UNIX nanosecond timestamp; validated to be a reasonable
            value (post-2020, pre-2030 to catch clock-skew exploits).
        details: Optional flat dict of auxiliary evidence metadata.

    Raises:
        ValidationError: On any constraint violation.
        ValueError: If ``amount`` is supplied without ``currency`` or vice-versa.
    """

    tx_id: TxIDStr
    tx_type: _TX_TYPES
    tenant_id: TenantIDStr
    initiator_bot: Annotated[
        str,
        Field(min_length=2, max_length=32, pattern=r"^[A-Z][A-Z0-9_]{1,31}$"),
    ]
    score: AnomalyScore
    amount: Annotated[Decimal | None, Field(default=None, ge=_MIN_AMOUNT, le=_MAX_AMOUNT)]
    currency: CurrencyStr | None = None
    timestamp_ns: Annotated[
        int,
        Field(
            ge=1_577_836_800_000_000_000,  # 2020-01-01T00:00:00Z in ns
            le=1_893_456_000_000_000_000,  # 2030-01-01T00:00:00Z in ns
        ),
    ]
    details: dict[str, _DETAIL_VALUE_TYPES] = Field(default_factory=dict)

    @field_validator("amount", mode="before")
    @classmethod
    def _coerce_amount(cls, v: Any) -> Decimal | None:
        if v is None:
            return None
        return _parse_decimal_amount(v)

    @field_validator("details", mode="before")
    @classmethod
    def _validate_details_field(cls, v: Any) -> dict[str, _DETAIL_VALUE_TYPES]:
        if v is None:
            return {}
        return _validate_details(v)

    @model_validator(mode="after")
    def _check_amount_currency_coherence(self) -> "TransactionRequest":
        """Amount and currency must both be present or both absent."""
        has_amount = self.amount is not None
        has_currency = self.currency is not None
        if has_amount != has_currency:
            raise ValueError(
                "amount and currency must both be provided or both omitted; "
                f"got amount={'<set>' if has_amount else 'null'}, "
                f"currency={'<set>' if has_currency else 'null'}"
            )
        return self


# ── PBFTEnvelopeRequest ───────────────────────────────────────────────────────

_PBFT_PHASES = Literal[
    "PRE_PREPARE",
    "PREPARE",
    "COMMIT",
    "VIEW_CHANGE",
    "NEW_VIEW",
    "CHECKPOINT",
]


class PBFTEnvelopeRequest(_BaseContract):
    """
    Payload for ``POST /api/consensus/envelope`` — submit a PBFT consensus
    envelope directly via the HTTP API (used by external replica nodes that
    cannot reach the gRPC mesh).

    All cryptographic fields are validated for correct length and hex encoding.
    The signature is *not* verified at the schema level — that is the job of
    ``PBFTNode.handle_envelope()``.

    Args:
        phase: PBFT message phase.
        view: Current view number (non-negative, fits int64).
        seq: Sequence number (non-negative, fits int64).
        digest: SHA-256 hex digest of the request payload.
        sender_id: Sending node's stable identifier.
        ed25519_pubkey: Hex-encoded 32-byte Ed25519 public key of the sender.
        signature: Hex-encoded 64-byte Ed25519 signature over the envelope fields.
        payload_b64: Base64-encoded inner payload bytes (max 64 KiB).
        timestamp_ns: Envelope creation time in UNIX nanoseconds.
        merkle_root: SHA-256 hex Merkle root at the time of envelope creation.

    Raises:
        ValidationError: On any hex pattern mismatch or out-of-range numeric value.
    """

    phase: _PBFT_PHASES
    view: UInt63
    seq: UInt63
    digest: SHA256Str
    sender_id: NodeIDStr
    ed25519_pubkey: Ed25519PubKeyStr
    signature: Ed25519SigStr
    payload_b64: Annotated[str, Field(min_length=0, max_length=87_381)]  # 64 KiB in base64
    timestamp_ns: Annotated[
        int,
        Field(
            ge=1_577_836_800_000_000_000,
            le=1_893_456_000_000_000_000,
        ),
    ]
    merkle_root: SHA256Str


