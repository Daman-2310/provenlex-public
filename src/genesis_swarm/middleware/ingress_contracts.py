# ── Base contract ─────────────────────────────────────────────────────────────


class _BaseContract(BaseModel):
    """
    Ancestor of every ingress schema.

    Config
    ------
    extra='forbid':
        Any field not declared in the model causes a ``ValidationError``.
        This is the primary defence against fat-payload / field-smuggling attacks.
    frozen=True:
        Model instances are immutable after construction; prevents accidental
        mutation in middleware or route handlers.
    str_strip_whitespace=True:
        Leading/trailing whitespace is stripped from all ``str`` fields before
        pattern validation.  Prevents homoglyph padding tricks.
    str_max_length=4096:
        Hard cap on any single ``str`` field to protect downstream buffers.
    validate_default=True:
        Default values are run through validators, catching misconfigured defaults
        early rather than silently storing invalid values.
    populate_by_name=False:
        Aliases cannot shadow field names; prevents confusing double-binding.
    """

    model_config = ConfigDict(
        extra="forbid",
        frozen=True,
        str_strip_whitespace=True,
        str_max_length=_MAX_BODY_STR_LEN,
        validate_default=True,
        populate_by_name=False,
    )


# ── Bounded free-text detail map ──────────────────────────────────────────────

_DETAIL_VALUE_TYPES = str | int | float | bool


def _validate_details(v: Any) -> dict[str, _DETAIL_VALUE_TYPES]:
    """
    Validate a free-form ``details`` mapping against injection-prevention rules.

    Args:
        v: The raw value to validate; must be a plain ``dict``.

    Returns:
        The validated ``dict[str, str | int | float | bool]``.

    Raises:
        ValueError: If ``v`` is not a dict, has too many keys, contains invalid
            key names, nested structures, or string values that exceed the cap.
    """
    if not isinstance(v, dict):
        raise ValueError("details must be a flat JSON object")
    if len(v) > _MAX_DETAILS_KEYS:
        raise ValueError(
            f"details must have at most {_MAX_DETAILS_KEYS} keys; got {len(v)}"
        )
    sanitised: dict[str, _DETAIL_VALUE_TYPES] = {}
    for raw_key, val in v.items():
        if not isinstance(raw_key, str):
            raise ValueError(f"details key must be a string; got {type(raw_key).__name__!r}")
        if len(raw_key) > _MAX_DETAIL_KEY_LEN:
            raise ValueError(
                f"details key {raw_key!r} exceeds {_MAX_DETAIL_KEY_LEN} chars"
            )
        if not _RE_DETAIL_KEY.match(raw_key):
            raise ValueError(
                f"details key {raw_key!r} must match [a-zA-Z][a-zA-Z0-9_.{{0,63}}]"
            )
        if isinstance(val, (dict, list)):
            raise ValueError(
                f"details[{raw_key!r}] is a nested structure; only scalars are allowed"
            )
        if isinstance(val, bool):
            sanitised[raw_key] = val
        elif isinstance(val, str):
            if len(val) > _MAX_DETAIL_STR_LEN:
                raise ValueError(
                    f"details[{raw_key!r}] string exceeds {_MAX_DETAIL_STR_LEN} chars"
                )
            sanitised[raw_key] = val
        elif isinstance(val, int):
            if val > _MAX_INT64 or val < -(_MAX_INT64 + 1):
                raise ValueError(
                    f"details[{raw_key!r}] integer outside int64 range"
                )
            sanitised[raw_key] = val
        elif isinstance(val, float):
            import math
            if not math.isfinite(val):
                raise ValueError(
                    f"details[{raw_key!r}] float is NaN or Inf — not allowed"
                )
            sanitised[raw_key] = val
        else:
            raise ValueError(
                f"details[{raw_key!r}] has unsupported type {type(val).__name__!r}"
            )
    return sanitised


def _parse_decimal_amount(v: Any) -> Decimal:
    """
    Convert a JSON-native amount representation to ``Decimal``.

    Rejects IEEE-754 ``float`` inputs to prevent precision loss on financial
    quantities.  Accepts integer cent/satoshi representations and decimal
    strings.

    Args:
        v: Raw input from JSON — expected to be ``str``, ``int``, or ``Decimal``.

    Returns:
        A ``Decimal`` value.

    Raises:
        ValueError: If ``v`` is a ``float``, an invalid decimal string, ``bool``,
            or any other unsupported type.
    """
    if isinstance(v, bool):
        raise ValueError("bool is not a valid amount type")
    if isinstance(v, float):
        raise ValueError(
            "Float amounts are rejected due to IEEE 754 precision risk. "
            "Send amounts as a JSON string (e.g., \"amount\": \"1234.56\") "
            "or as an integer number of smallest currency units."
        )
    if isinstance(v, Decimal):
        return v
    if isinstance(v, int):
        return Decimal(v)
    if isinstance(v, str):
        try:
            return Decimal(v)
        except InvalidOperation as exc:
            raise ValueError(f"Invalid decimal string {v!r}: {exc}") from exc
    raise ValueError(f"Unsupported amount type {type(v).__name__!r}")


# ── Sub-model: MonetaryAmount ─────────────────────────────────────────────────


class MonetaryAmount(_BaseContract):
    """
    A validated monetary quantity with an ISO 4217 currency code.

    The ``amount`` field rejects IEEE-754 ``float`` inputs and enforces both a
    minimum positive value and a maximum that fits within a 64-bit financial
    representation.

    Args:
        amount: The monetary amount as a decimal string, integer, or ``Decimal``.
        currency: ISO 4217 three-letter currency code (e.g., ``"EUR"``).

    Raises:
        ValidationError: If ``amount`` is a float, negative, zero, or overflows;
            or if ``currency`` does not match ``^[A-Z]{3}$``.
    """

    amount: Annotated[Decimal, Field(ge=_MIN_AMOUNT, le=_MAX_AMOUNT)]
    currency: CurrencyStr

    @field_validator("amount", mode="before")
    @classmethod
    def _coerce_amount(cls, v: Any) -> Decimal:
        return _parse_decimal_amount(v)


