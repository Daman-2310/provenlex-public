# ── EscrowRequest ─────────────────────────────────────────────────────────────

_ESCROW_CONDITIONS = Literal["TIME_LOCK", "MULTI_SIG", "ORACLE_TRIGGER", "PBFT_QUORUM"]


class EscrowRequest(_BaseContract):
    """
    Payload for ``POST /api/v1/escrow`` — create a new escrow contract.

    Args:
        escrow_id: Client-generated unique escrow identifier.
        payer_tenant_id: Tenant depositing funds into escrow.
        beneficiary_tenant_id: Tenant to receive funds upon condition satisfaction.
        amount: Escrowed monetary amount (decimal string or integer).
        currency: ISO 4217 currency code.
        condition_type: The release condition mechanism.
        release_at_ns: UNIX nanosecond timestamp; required for ``TIME_LOCK``
            conditions; ignored for others.
        required_signers: For ``MULTI_SIG`` — a list of signer node IDs (2–11).
        oracle_endpoint: For ``ORACLE_TRIGGER`` — an HTTPS URL to the price
            oracle (max 512 chars, must start with ``https://``).
        details: Optional auxiliary metadata.

    Raises:
        ValidationError: On constraint violation.
        ValueError: If ``TIME_LOCK`` is missing ``release_at_ns``, or
            ``MULTI_SIG`` has the wrong number of signers.
    """

    escrow_id: Annotated[
        str,
        Field(min_length=15, max_length=41, pattern=r"^escrow-[a-zA-Z0-9]{8,32}$"),
    ]
    payer_tenant_id: TenantIDStr
    beneficiary_tenant_id: TenantIDStr
    amount: Annotated[Decimal, Field(ge=_MIN_AMOUNT, le=_MAX_AMOUNT)]
    currency: CurrencyStr
    condition_type: _ESCROW_CONDITIONS
    release_at_ns: Annotated[int | None, Field(ge=0, le=_MAX_INT64)] = None
    required_signers: list[NodeIDStr] = Field(default_factory=list, max_length=11)
    oracle_endpoint: Annotated[
        str | None,
        Field(
            default=None,
            min_length=8,
            max_length=512,
            pattern=r"^https://[a-zA-Z0-9._/-]{1,490}$",
        ),
    ] = None
    details: dict[str, _DETAIL_VALUE_TYPES] = Field(default_factory=dict)

    @field_validator("amount", mode="before")
    @classmethod
    def _coerce_amount(cls, v: Any) -> Decimal:
        return _parse_decimal_amount(v)

    @field_validator("details", mode="before")
    @classmethod
    def _validate_details_field(cls, v: Any) -> dict[str, _DETAIL_VALUE_TYPES]:
        if v is None:
            return {}
        return _validate_details(v)

    @model_validator(mode="after")
    def _validate_condition_fields(self) -> "EscrowRequest":
        """Enforce condition-type-specific required fields."""
        if self.condition_type == "TIME_LOCK" and self.release_at_ns is None:
            raise ValueError("TIME_LOCK escrows require release_at_ns")
        if self.condition_type == "MULTI_SIG":
            if len(self.required_signers) < 2:
                raise ValueError(
                    "MULTI_SIG escrows require at least 2 required_signers"
                )
            if len(set(self.required_signers)) != len(self.required_signers):
                raise ValueError("required_signers must not contain duplicates")
        if self.condition_type == "ORACLE_TRIGGER" and self.oracle_endpoint is None:
            raise ValueError("ORACLE_TRIGGER escrows require oracle_endpoint")
        if self.payer_tenant_id == self.beneficiary_tenant_id:
            raise ValueError("payer and beneficiary must be different tenants")
        return self


